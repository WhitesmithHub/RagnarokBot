def render_class_preview(class_key: str) -> str:
    """HTML-–∫–∞—Ä—Ç–æ—á–∫–∞: –æ–ø–∏—Å–∞–Ω–∏–µ + –∞–∫—Ç–∏–≤–Ω—ã–µ –∏ –ø–∞—Å—Å–∏–≤–Ω—ã–µ —É–º–µ–Ω–∏—è, –æ—Ç–º–µ—Ç–∫–∞ —Å—Ç–∞—Ä—Ç–æ–≤–æ–≥–æ —É–º–µ–Ω–∏—è."""
    desc = CLASS_DESCRIPTIONS[class_key]
    ab = CLASS_ABILITIES[class_key]
    lines = [desc, "\n<b>–£–º–µ–Ω–∏—è:</b>"]

    lines.append("\n<i>–ê–∫—Ç–∏–≤–Ω—ã–µ</i>:")
    for a in ab["active"]:
        lines.append(f"‚Ä¢ {a['title']} ‚Äî {a['text']}")

    lines.append("\n<i>–ü–∞—Å—Å–∏–≤–Ω—ã–µ</i>:")
    for a in ab["passive"]:
        lines.append(f"‚Ä¢ {a['title']} ‚Äî {a['text']}")

    starter = next(t for t in ab["active"] if t["key"] == ab["starter_key"])
    lines.append(f"\n<b>–í –Ω–∞—á–∞–ª–µ –¥–æ—Å—Ç—É–ø–Ω–æ —Ç–æ–ª—å–∫–æ –æ–¥–Ω–æ —É–º–µ–Ω–∏–µ:</b> {starter['title']}")
    return "\n".join(lines)

def starter_and_pool(class_key: str):
    """–í–µ—Ä–Ω—ë—Ç (starter_key, pool_keys:list[str])"""
    ab = CLASS_ABILITIES[class_key]
    starter = ab["starter_key"]
    pool = [x["key"] for x in (ab["active"] + ab["passive"]) if x["key"] != starter]
    return starter, pool
